const mkPreset = (
  id: string,
  slug: string,
  title: string,
  description: string,
  category: PromptSettings["useCase"],
  icon: string,
  outputStyle: string,
  details: {
    tone?: PromptSettings["tone"];
    targetModel?: string;
    outputFormat?: OutputFormat;
    detailLevel?: PromptSettings["detailLevel"];
    hasAudience?: boolean;
    hasContext?: boolean;
    hasConstraints?: boolean;
    hasExamples?: boolean;
  },
  defaults: Partial<PromptSettings>,
  exampleInputs: TemplatePreset["exampleInputs"],
): TemplatePreset => ({
  id,
  slug,
  title,
  description,
  category,
  icon,
  recommendedFields: details,
  defaultSettings: { ...defaultBase, ...defaults },
  exampleInputs,
  outputStyle,
});

